#ifndef STD_TYPE_H_
#define STD_TYPE_H_




typedef unsigned char uint8;		/* define unit8 as 8 bit unsigned data type */
typedef unsigned short uint16;		/* define unit16 as 16 bit unsigned data type */
typedef unsigned long uint32;		/* define unit32 as 32 bit unsigned data type */

typedef signed char sint8;		/* define snit8 as 8 bit signed data type */
typedef signed short sint16;		/* define snit16 as 16 bit signed data type */
typedef signed long sint32;		/* define snit32 as 32 bit signed data type */

#endif /* STD_TYPE_H_ */
